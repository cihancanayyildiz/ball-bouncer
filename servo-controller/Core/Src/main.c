void CDC_ReceiveCallBack(uint8_t *buffer, uint32_t size)
{
	char temp[4];

	if (size == 4)
	{
		memcpy(temp, buffer, size);

		if (strcmp(temp, key) == 0)
		{
			CDC_Transmit_FS(buffer, size);
		}
	}
	else if (size == 8)
	{
		memcpy(&incoming, buffer, size);

		if (strcmp(incoming.key, key) == 0)
		{
			servo_sweep(incoming);
		}
	}
}

